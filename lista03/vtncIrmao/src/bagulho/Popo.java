package bagulho;

public class Popo {
    int a, b;
    
    public Popo() {
        this(1, 2);
    }
    public Popo(int a, int b) {
        this.a = a;
        this.b = b;
    }
    
    public int getA() {
        return this.a;
    }
    
    public int getB() {
        return this.b;
    }
    
}
